// Asegúrate de tener Metamask instalada y conectada
window.addEventListener('load', async () => {
    if (typeof window.ethereum !== 'undefined') {
        console.log('MetaMask está instalada');

        await ethereum.request({ method: 'eth_requestAccounts' }); // Solicita acceso a MetaMask

        const chainId = await ethereum.request({ method: 'eth_chainId' });
        const amoyChainId = '0x13881';  // Chain ID 80002 en formato hexadecimal

        if (chainId !== amoyChainId) {
            alert('Por favor, cambia a la red AMOY en MetaMask');
        } else {
            console.log('Conectado a la red AMOY');
            window.web3 = new Web3(window.ethereum);  // Conectar a MetaMask y red AMOY
        }
    } else {
        alert('Por favor, instala MetaMask');
    }
});

